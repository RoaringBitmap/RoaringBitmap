  private static <T> Iterator<T> toIterator(final T[] x) {
    return new Iterator<T>() {
      int pos = 0;

      @Override
      public boolean hasNext() {
        return pos < x.length;
      }

      @Override
      public void remove() {}

      @Override
      public T next() {
        return x[pos++];
      }
    };
  }

